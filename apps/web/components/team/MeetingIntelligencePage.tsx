function downloadHtml(title: string, analysis: MeetingAnalysis) {
  const blob = new Blob([buildHtmlContent(title, analysis)], { type: 'text/html' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = `meeting-${title.toLowerCase().replace(/\s+/g, '-')}.html`
  a.click()
  URL.revokeObjectURL(url)
}


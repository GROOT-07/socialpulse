// ── Sentiment badge ───────────────────────────────────────────

const SENTIMENT_STYLES: Record<string, string> = {
  POSITIVE: 'bg-green-500/15 text-green-400 border-green-500/30',
  NEUTRAL:  'bg-slate-500/15 text-slate-400 border-slate-500/30',
  MIXED:    'bg-amber-500/15 text-amber-400 border-amber-500/30',
  NEGATIVE: 'bg-red-500/15 text-red-400 border-red-500/30',
}

const PRIORITY_STYLES: Record<string, string> = {
  HIGH:   'bg-red-500/15 text-red-400',
  MEDIUM: 'bg-amber-500/15 text-amber-400',
  LOW:    'bg-slate-500/15 text-slate-400',
}

// ── Download helpers ──────────────────────────────────────────

function buildTxtContent(title: string, analysis: MeetingAnalysis): string {
  return [
    `MEETING INTELLIGENCE REPORT`,
    `═══════════════════════════════════════`,
    `Title: ${title}`,
    `Date: ${analysis.date}`,
    `Duration: ${analysis.duration}`,
    `Participants: ${analysis.participants.join(', ')}`,
    `Sentiment: ${analysis.sentiment}`,
    ``,
    `EXECUTIVE SUMMARY`,
    `─────────────────`,
    analysis.executiveSummary,
    ``,
    `KEY DECISIONS`,
    `─────────────`,
    ...(analysis.keyDecisions.map((d, i) => `${i + 1}. ${d}`)),
    ``,
    `ACTION ITEMS`,
    `────────────`,
    ...(analysis.actionItems.map((a, i) =>
      `${i + 1}. [${a.priority}] ${a.task}\n   Owner: ${a.owner} | Deadline: ${a.deadline}`
    )),
    ``,
    `CONTENT IDEAS`,
    `─────────────`,
    ...(analysis.contentIdeas.map((c, i) =>
      `${i + 1}. ${c.title}\n   Platform: ${c.platform} | Format: ${c.format}\n   Hook: ${c.hook}`
    )),
    ``,
    `FOLLOW-UP QUESTIONS`,
    `───────────────────`,
    ...(analysis.followUpQuestions.map((q, i) => `${i + 1}. ${q}`)),
    ``,
    `NEXT MEETING AGENDA`,
    `───────────────────`,
    ...(analysis.nextMeetingAgenda.map((a, i) => `${i + 1}. ${a}`)),
    ``,
    `───────────────────────────────────────`,
    `Generated by SocialPulse AI Meeting Intelligence`,
  ].join('\n')
}

function downloadTxt(title: string, analysis: MeetingAnalysis) {
  const content = buildTxtContent(title, analysis)
  const blob = new Blob([content], { type: 'text/plain' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = `meeting-${title.toLowerCase().replace(/\s+/g, '-')}.txt`
  a.click()
  URL.revokeObjectURL(url)
}

function downloadJson(title: string, analysis: MeetingAnalysis) {
  const blob = new Blob([JSON.stringify({ title, ...analysis }, null, 2)], { type: 'application/json' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = `meeting-${title.toLowerCase().replace(/\s+/g, '-')}.json`
  a.click()
  URL.revokeObjectURL(url)
}


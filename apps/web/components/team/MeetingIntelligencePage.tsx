// ── Analyze form ──────────────────────────────────────────────

function AnalyzeForm({ onClose }: { onClose: () => void }) {
  const qc = useQueryClient()
  const [title, setTitle] = useState('')
  const [transcript, setTranscript] = useState('')
  const [participants, setParticipants] = useState('')
  const [duration, setDuration] = useState('')
  const [result, setResult] = useState<{ id: string; title: string; analysis: MeetingAnalysis } | null>(null)

  const analyzeMutation = useMutation({
    mutationFn: () =>
      teamApi.analyzeMeeting({
        title: title || undefined,
        transcript,
        participants: participants || undefined,
        duration: duration || undefined,
      }),
    onSuccess: (data) => {
      setResult(data)
      void qc.invalidateQueries({ queryKey: ['team', 'meetings'] })
    },
  })

  if (result) {
    // Show quick summary of what was just analyzed
    return (
      <Card className="border-green-500/40">
        <CardContent className="p-5">
          <div className="flex items-start gap-3 mb-4">
            <div className="flex h-9 w-9 shrink-0 items-center justify-center rounded-lg bg-green-500/15 text-green-400">
              <Sparkles className="h-5 w-5" />
            </div>
            <div>
              <p className="font-semibold text-[var(--color-text)]">Analysis complete!</p>
              <p className="text-xs text-[var(--color-text-4)] mt-0.5">
                {result.analysis.actionItems.length} action items · {result.analysis.contentIdeas.length} content ideas
              </p>
            </div>
          </div>
          <p className="text-sm text-[var(--color-text-2)] rounded-lg bg-surface-2 border border-brand-border p-3 leading-relaxed">
            {result.analysis.executiveSummary}
          </p>
          <div className="mt-4 flex items-center gap-2">
            <Button
              size="sm"
              variant="outline"
              className="h-7 text-xs"
              onClick={() => downloadTxt(result.title, result.analysis)}
            >
              <Download className="h-3 w-3 mr-1" /> TXT
            </Button>
            <Button
              size="sm"
              variant="outline"
              className="h-7 text-xs"
              onClick={() => downloadHtml(result.title, result.analysis)}
            >
              <Download className="h-3 w-3 mr-1" /> HTML
            </Button>
            <Button
              size="sm"
              className="h-7 text-xs ml-auto gap-1"
              onClick={onClose}
            >
              <Check className="h-3 w-3" /> Done
            </Button>
          </div>
        </CardContent>
      </Card>
    )
  }

  return (
    <Card className="border-[var(--color-accent)]/40">
      <CardContent className="p-5">
        <div className="flex items-center justify-between mb-4">
          <div className="flex items-center gap-2">
            <Mic className="h-4 w-4 text-[var(--color-accent)]" />
            <p className="font-semibold text-sm text-[var(--color-text)]">Analyze Meeting</p>
          </div>
          <Button size="icon" variant="ghost" onClick={onClose} className="h-7 w-7">
            <X className="h-3.5 w-3.5" />
          </Button>
        </div>

        <div className="grid grid-cols-1 gap-3 sm:grid-cols-3 mb-3">
          <div className="sm:col-span-1">
            <label className="mb-1.5 block text-xs font-medium text-[var(--color-text-3)]">
              Meeting Title
            </label>
            <Input
              value={title}
              onChange={(e) => setTitle(e.target.value)}
              placeholder="e.g. Q2 Content Planning"
              className="text-sm"
            />
          </div>
          <div>
            <label className="mb-1.5 block text-xs font-medium text-[var(--color-text-3)]">
              Duration
            </label>
            <Input
              value={duration}
              onChange={(e) => setDuration(e.target.value)}
              placeholder="e.g. 45 minutes"
              className="text-sm"
            />
          </div>
          <div>
            <label className="mb-1.5 block text-xs font-medium text-[var(--color-text-3)]">
              Participants (comma-separated)
            </label>
            <Input
              value={participants}
              onChange={(e) => setParticipants(e.target.value)}
              placeholder="Mani, Priya, Rahul"
              className="text-sm"
            />
          </div>
        </div>

        <div className="mb-4">
          <label className="mb-1.5 block text-xs font-medium text-[var(--color-text-3)]">
            Meeting Transcript / Notes *
          </label>
          <Textarea
            value={transcript}
            onChange={(e) => setTranscript(e.target.value)}
            placeholder="Paste your meeting transcript, notes, or key points here. The AI will extract action items, decisions, content ideas, and generate a structured summary.

Example:
Mani: We need to ramp up our Instagram presence this quarter...
Priya: I think we should focus on Reels, the engagement is much higher...
Rahul: Let's set a target of 5 Reels per week..."
            rows={10}
            className="text-sm resize-none font-mono"
          />
          <p className="mt-1 text-[10px] text-[var(--color-text-4)]">
            Minimum 50 characters · Maximum 50,000 characters
          </p>
        </div>

        {analyzeMutation.isError && (
          <div className="mb-4 flex items-center gap-2 rounded-lg bg-red-500/10 border border-red-500/20 p-3 text-sm text-red-400">
            <AlertTriangle className="h-4 w-4 shrink-0" />
            Analysis failed. Please check your input and try again.
          </div>
        )}

        <Button
          onClick={() => analyzeMutation.mutate()}
          disabled={transcript.trim().length < 50 || analyzeMutation.isPending}
          className="w-full gap-2"
        >
          {analyzeMutation.isPending ? (
            <>
              <Loader2 className="h-4 w-4 animate-spin" />
              Analyzing with AI… (this takes 10–20 seconds)
            </>
          ) : (
            <>
              <Sparkles className="h-4 w-4" />
              Analyze Meeting
            </>
          )}
        </Button>
      </CardContent>
    </Card>
  )
}

// ── Page ──────────────────────────────────────────────────────

export function MeetingIntelligencePage() {
  const qc = useQueryClient()
  const [analyzing, setAnalyzing] = useState(false)

  const { data: meetings = [], isLoading, isFetching, refetch } = useQuery({
    queryKey: ['team', 'meetings'],
    queryFn: () => teamApi.listMeetings(),
    staleTime: 60_000,
  })

  const deleteMutation = useMutation({
    mutationFn: (id: string) => teamApi.deleteMeeting(id),
    onSuccess: () => void qc.invalidateQueries({ queryKey: ['team', 'meetings'] }),
  })

  return (
    <>
      <PageHeader
        title="Meeting Intelligence"
        description="Paste any meeting transcript and AI extracts action items, decisions, content ideas, and a structured report."
        icon={<Mic className="h-5 w-5" />}
        actions={
          <div className="flex items-center gap-2">
            <Button
              variant="ghost"
              size="sm"
              onClick={() => refetch()}
              disabled={isFetching}
              className="h-8 w-8 p-0"
            >
              <RefreshCw className={`h-3.5 w-3.5 ${isFetching ? 'animate-spin' : ''}`} />
            </Button>
            {!analyzing && (
              <Button
                size="sm"
                className="h-8 gap-1.5"
                onClick={() => setAnalyzing(true)}
              >
                <Plus className="h-3.5 w-3.5" />
                Analyze Meeting
              </Button>
            )}
          </div>
        }
      />

      {/* Analyze form */}
      {analyzing && (
        <div className="mt-6">
          <AnalyzeForm onClose={() => setAnalyzing(false)} />
        </div>
      )}

      {/* Loading skeleton */}
      {isLoading && (
        <div className="mt-6 space-y-3">
          {Array.from({ length: 3 }).map((_, i) => (
            <div key={i} className="h-20 rounded-xl bg-surface-2 animate-pulse" />
          ))}
        </div>
      )}

      {/* Empty state */}
      {!isLoading && meetings.length === 0 && !analyzing && (
        <Card className="mt-6">
          <CardContent className="p-0">
            <EmptyState
              icon={<Mic className="h-12 w-12" />}
              heading="No meeting analyses yet"
              description="Paste a meeting transcript or notes and let AI extract action items, decisions, content ideas, and a structured report you can download."
              action={{ label: 'Analyze First Meeting', onClick: () => setAnalyzing(true) }}
            />
          </CardContent>
        </Card>
      )}

      {/* Meetings list */}
      {!isLoading && meetings.length > 0 && (
        <div className="mt-6 space-y-3">
          {meetings.map((meeting) => (
            <AnalysisCard
              key={meeting.id}
              summary={meeting}
              onDelete={() => deleteMutation.mutate(meeting.id)}
            />
          ))}
        </div>
      )}
    </>
  )
}
